// Get state by ID
export const getStateById = (stateId) => {
  return indiaStates.find((state) => state.id === stateId) || indiaStates[0];
};

// Get cities by state (for route planner filtering)
export const getCitiesByState = (stateId) => {
  return Object.entries(indiaCities)
    .filter(([_, data]) => data.state === stateId)
    .map(([name, data]) => ({ name, ...data }))
    .sort((a, b) => {
      // Sort by tier (1, 2, 3) then by name
      if (a.tier !== b.tier) return a.tier - b.tier;
      return a.name.localeCompare(b.name);
    });
};

// Search cities by query (for autocomplete)
export const searchCities = (query, stateId = null) => {
  if (!query || query.length < 2) return [];
  
  const lowerQuery = query.toLowerCase();
  let cities = Object.entries(indiaCities)
    .filter(([name, data]) => {
      const matchesQuery = name.toLowerCase().includes(lowerQuery);
      const matchesState = !stateId || data.state === stateId;
      return matchesQuery && matchesState;
    })
    .map(([name, data]) => ({ name, ...data }))
    .sort((a, b) => {
      // Prioritize tier 1 cities, then by relevance
      if (a.tier !== b.tier) return a.tier - b.tier;
      return a.name.toLowerCase().indexOf(lowerQuery) - b.name.toLowerCase().indexOf(lowerQuery);
    });
  
  return cities.slice(0, 20); // Limit to 20 results for performance
};
